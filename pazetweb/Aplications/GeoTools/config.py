
from qgis.core import *

def run():
    # ---------------------------------------------------------------------------------------
    QgsApplication.setPrefixPath("/usr/share/qgis", True)
    qgs = QgsApplication([], True)
    qgs.initQgis()
    # ---------------------------------------------------------------------------------------
    return qgs


def stop(qgs):
    # ---------------------------------------------------------------------------------------
    qgs.exitQgis()
    # ---------------------------------------------------------------------------------------